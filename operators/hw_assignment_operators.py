print(result)
# операторы: +=, *, -
# 1) 3 * 4 = 12
# 2) 3 * 4 - 6 = 12 - 6 = 6
# 3) result += 3 * 4 - 6 => 2 += 6 => 2 + 6 = 8

x, y, z, i, j = 10, 20, 30, 40, 50
