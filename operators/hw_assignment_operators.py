print(result)
# операторы: =, +, -, *, /
# (y + 1) - скобки не нужны
# 1) z - 1 = 30 - 1 = 29
# 2) i + 1 = 40 + 1 = 41
# 3) (z - 1) * (i + 1) = (29) * (41) = 1189
# 4) (z - 1) * (i + 1) / j = 1189 / 50 = 23.78
# 5) x + 1 + (y + 1) = 10 + 1 + 20 + 1 = 32
